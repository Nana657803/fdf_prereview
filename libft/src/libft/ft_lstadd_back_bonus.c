#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new_node)
{
	t_list	*last;

	if (!lst || !new_node)
		return ;
	if (*lst)
	{
		last = ft_lstlast(*lst);
		last->next = new_node;
	}
	else
		*lst = new_node;
}
